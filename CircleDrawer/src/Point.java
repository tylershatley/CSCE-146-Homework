
public class Point {

	private double x,y;
	
	public Point(double xx, double xy)
	{
		this.setx(xx);
		this.setY(xy);
	}

	public double getx() {
		return x;
	}

	public void setx(double xx) {
		this.x = xx;
	}

	public double gety() {
		return y;
	}

	public void setY(double xy) {
		this.y = xy;
	}
	
	
}
